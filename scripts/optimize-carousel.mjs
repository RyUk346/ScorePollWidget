// ---------------------------------------------------------------------------
// Carousel image optimizer
//
// Converts the large PNG/JPG screenshots in public/images/ into resized WebP
// files (much smaller, so the carousel loads fast), backs up the originals to
// image-originals/ (kept out of the deployed build), and rewrites
// src/data/carousel.js to reference the optimized .webp files.
//
// Run once (and again whenever you add new images):
//   npm install          # installs sharp (added to devDependencies)
//   npm run optimize:images
// ---------------------------------------------------------------------------
import { fileURLToPath } from "node:url";
import path from "node:path";
import fs from "node:fs/promises";
import sharp from "sharp";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const IMAGES_DIR = path.join(ROOT, "public", "images");
const BACKUP_DIR = path.join(ROOT, "image-originals");
const CAROUSEL_JS = path.join(ROOT, "src", "data", "carousel.js");

// Tune these if you want sharper/smaller images.
const MAX_WIDTH = 1000; // px — plenty for the on-screen size (incl. retina)
const QUALITY = 78; // WebP quality 0–100

const RASTER = /\.(png|jpe?g)$/i;
const kb = (bytes) => `${(bytes / 1024).toFixed(0)} KB`;

async function main() {
  let entries;
  try {
    entries = await fs.readdir(IMAGES_DIR);
  } catch {
    console.error(`No public/images directory at ${IMAGES_DIR}`);
    process.exit(1);
  }

  const sources = entries.filter((f) => RASTER.test(f)).sort();
  if (!sources.length) {
    console.log("No PNG/JPG files to optimize (already WebP?).");
  }

  await fs.mkdir(BACKUP_DIR, { recursive: true });

  let before = 0;
  let after = 0;
  for (const file of sources) {
    const src = path.join(IMAGES_DIR, file);
    const base = file.replace(RASTER, "");
    const out = path.join(IMAGES_DIR, `${base}.webp`);

    const srcSize = (await fs.stat(src)).size;
    await sharp(src)
      .rotate() // respect EXIF orientation
      .resize({ width: MAX_WIDTH, withoutEnlargement: true })
      .webp({ quality: QUALITY })
      .toFile(out);
    const outSize = (await fs.stat(out)).size;

    before += srcSize;
    after += outSize;

    // Move the original out of public/ so it isn't copied into the build.
    await fs.rename(src, path.join(BACKUP_DIR, file));
    console.log(`  ${file}  ${kb(srcSize)} -> ${base}.webp  ${kb(outSize)}`);
  }

  // Collect the final image list (all .webp now in the folder), in name order.
  const finalEntries = (await fs.readdir(IMAGES_DIR))
    .filter((f) => /\.webp$/i.test(f))
    .sort();

  const fileLines = finalEntries.map((f) => `  ${JSON.stringify(f)},`).join("\n");
  const carousel = `// Images for the vote-page carousel. Files live in public/images/.
// This list is regenerated by \`npm run optimize:images\` — names must match
// the files exactly (they're URL-encoded automatically below).
const FILES = [
${fileLines}
];

const CAROUSEL_IMAGES = FILES.map(
  (f) => \`\${import.meta.env.BASE_URL}images/\${encodeURIComponent(f)}\`,
);

export default CAROUSEL_IMAGES;
`;
  await fs.writeFile(CAROUSEL_JS, carousel, "utf8");

  if (sources.length) {
    const saved = before - after;
    const pct = before ? ((saved / before) * 100).toFixed(1) : "0";
    console.log(
      `\nDone. ${sources.length} images: ${kb(before)} -> ${kb(after)} (saved ${kb(saved)}, ${pct}%).`,
    );
    console.log(`Originals backed up to: ${BACKUP_DIR}`);
  }
  console.log(`Updated ${path.relative(ROOT, CAROUSEL_JS)} with ${finalEntries.length} images.`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
